
# Iteration using an for and while loop in python in different data types


# Ques no. 1
# Python program to print an numbers from 1 to 10 using an while loop
print("\n")
i = 1
print("Printing an numbers from 1 to 10 using an while loop in python: ")
while i <= 10:
    print(i,end=",")
    i = i + 1

print("\n\n")

# Ques no. 2
# Python program to print an strings by characters
name = "Netra Bahadur Khatri"
print("Printing an name by characters: ")
for a in name:
    print(a,end=", ")

print("\n")

